public class Ejercicio02 {

    public void comparar(int a, int b)
    {
        if (a < b)
        {
            System.out.println(a + " es menor que " + b);
        }
        else if (a > b)
        {
            System.out.println(a + " es mayor que " + b);
        }
        else
        {
            System.out.println(a + " es igual que " + b);
        }
    }
}
